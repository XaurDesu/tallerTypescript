export class Student {
    Codigo: String;
    Cedula: String;
    Edad: String;
    Direccion: String;
    Telefono: String;
    constructor(    Codigo: String,
        Cedula: String,
        Edad: String,
        Direccion: String,
        Telefono: String,)
        {
            this.Codigo = Codigo;
            this.Cedula = Cedula;
            this.Edad = Edad;
            this.Direccion = Direccion;
            this.Telefono = Telefono
        }
}